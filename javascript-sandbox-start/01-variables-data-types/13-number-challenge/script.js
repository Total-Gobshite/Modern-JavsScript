const x = Math.ceil(Math.random() * 100 + 1);
const y = Math.ceil(Math.random() * 50 + 1);

const sum = x + y;
console.log(`The sum of ${x} and ${y} is ${sum}`);

const difference = x - y;
console.log(`The difference between ${x} and ${y} is ${difference}`);

const product = x * y;
console.log(`The product of ${x} and ${y} is ${product}`);

const quotient = x / y;
console.log(`The quotient of ${x} and ${y} is ${quotient}`);

const remainder = x % y;
console.log(`The remainder of ${x} and ${y} is ${remainder}`);
